export function getRetreatBySlug(slug: string): Retreat | undefined {
  return retreats.find((r) => r.slug === slug);
}

export function getUpcomingRetreats(): Retreat[] {
  const now = new Date();
  return retreats
    .filter((r) => new Date(r.endDate + 'T23:59:59') >= now)
    .filter((r) => r.status !== 'sold_out')
    .sort((a, b) => new Date(a.startDate).getTime() - new Date(b.startDate).getTime());
}
